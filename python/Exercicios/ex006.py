# OBTENDO O DOBRO, TRIPLO E RAIZ QUADRADA DE UM NÚMERO

al = float(input('Digite um numero:'))
dobro = al * 2
triplo = al * 3
rq = al ** (1/2)
print ('O dobro desse numero é {},\n o triplo é {}\n e sua raiz quadrada é {:.3f}'.format(dobro, triplo, rq))

