# CONVERSOR DE TEMPERATURA DE GRAUS °F PARA GRAUS °C

grausf = float(input('Informe a temperatura em graus °F:'))
celsius = (grausf - 32) / 1.8
print(f'{grausf}°F equivale a {celsius}')

